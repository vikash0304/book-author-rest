package com.asnsignment.project.bookauthor.model;

public class AuthorRequest {

	private String name;
	private String book;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBook() {
		return book;
	}

	public void setBook(String book) {
		this.book = book;
	}

}
